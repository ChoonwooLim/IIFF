def seed_posts():
    db = SessionLocal()
    try:
        # Get admin user
        admin = db.query(User).filter(User.role == "superadmin").first()
        if not admin:
            print("  No superadmin user found. Run seed_admin.py first.")
            return

        # Check if posts already exist
        existing_count = db.query(Post).count()
        if existing_count > 0:
            print(f"  {existing_count} posts already exist, skipping.")
            return

        for board_slug, posts_data in SAMPLE_POSTS.items():
            board = db.query(Board).filter(Board.slug == board_slug).first()
            if not board:
                print(f"  Board '{board_slug}' not found, skipping.")
                continue

            for post_data in posts_data:
                post = Post(
                    board_id=board.id,
                    user_id=admin.id,
                    title=post_data["title"],
                    content=post_data["content"],
                    youtube_url=post_data.get("youtube_url"),
                    is_pinned=post_data.get("is_pinned", False),
                )
                db.add(post)
                print(f"  [{board_slug}] {post_data['title']}")

        db.commit()
        print(f"Post seeding complete. Created {sum(len(v) for v in SAMPLE_POSTS.values())} posts.")
    finally:
        db.close()


if __name__ == "__main__":
    seed_posts()
